    private boolean validarForm() {
        boolean isValidForm = true;
        StringBuilder errorMessageBuilder = new StringBuilder();
        
        //dni
        String dni = dniTextField.getText();
        if(StringUtils.isBlank(dni.trim())){
            errorMessageBuilder.append("El dni no puede estar vacío\n");
            isValidForm = false;
        }
        if(dni.length() < 6 || dni.length() > 9){
            errorMessageBuilder.append("Longitud de dni inválida\n");
            isValidForm = false;
        }
        if(!StringUtils.isNumeric(dni)){
            errorMessageBuilder.append("El dni sólo puede contener números\n");
            isValidForm = false;
        }
        
        //nombre
        String nombre = nombreTextField.getText();
        if(StringUtils.isBlank(nombre.trim())){
            errorMessageBuilder.append("El nombre no puede estar vacío\n");
            isValidForm = false;
        }
        if(!StringUtils.isAlphaSpace(nombre)){
            errorMessageBuilder.append("El nombre no puede contener números ni caracteres especiales\n");
            isValidForm = false;
        }
        
        //apellido
        String apellido = apellidoTextField.getText();
        if(StringUtils.isBlank(apellido.trim())){
            errorMessageBuilder.append("El apellido no puede estar vacío\n");
            isValidForm = false;
        }
        if(!StringUtils.isAlphaSpace(apellido)){
            errorMessageBuilder.append("El apellido no puede contener números ni caracteres especiales\n");
            isValidForm = false;
        }
        
        //promedio
        String promedio = promedioTextField.getText().trim();
        if(StringUtils.isBlank(promedio)){
            errorMessageBuilder.append("El promedio no puede estar vacío\n");
            isValidForm = false;
        } else {
            try {
                double prom = Double.parseDouble(promedio.replace(",", "."));
                if(prom < 0 || prom > 10){
                    errorMessageBuilder.append("El promedio debe estar entre 0 y 10\n");
                    isValidForm = false;
                }
            } catch (NumberFormatException e) {
                errorMessageBuilder.append("El promedio debe ser un número\n");
                isValidForm = false;
            }
        }

        //cantidad materias aprobadas
        String cantMat = cantMatAprobTextField.getText().trim();
        if(StringUtils.isBlank(cantMat)){
            errorMessageBuilder.append("La cantidad de materias aprobadas no puede estar vacía\n");
            isValidForm = false;
        } else if(!StringUtils.isNumeric(cantMat)){
            errorMessageBuilder.append("La cantidad de materias aprobadas debe ser un número entero\n");
            isValidForm = false;
        }

        //fecha nacimiento
        Calendar fecNacCalendar = fecNacDateChooser.getCalendar();
        if(fecNacCalendar == null){
            errorMessageBuilder.append("La fecha de nacimiento no puede estar vacía\n");
            isValidForm = false;
        } else if(fecNacCalendar.after(Calendar.getInstance())){
            errorMessageBuilder.append("La fecha de nacimiento no puede ser posterior al día actual\n");
            isValidForm = false;
        }

        //fecha ingreso
        Calendar calendar = fecIngDateChooser.getCalendar();
        if(calendar != null){
            if(calendar.after(Calendar.getInstance())){
                errorMessageBuilder.append("La fecha de ingreso no puede ser posterior al día actual\n");
                isValidForm = false;
            }
            if(!calendar.isSet(Calendar.DAY_OF_MONTH)){
                errorMessageBuilder.append("La fecha de ingreso debe contener un día\n");
                isValidForm = false;
            }
            if(!calendar.isSet(Calendar.MONTH)){
                errorMessageBuilder.append("La fecha de ingreso debe contener un mes\n");
                isValidForm = false;
            }
            if(!calendar.isSet(Calendar.YEAR)){
                errorMessageBuilder.append("La fecha de ingreso debe contener un año\n");
                isValidForm = false;
            }

            Calendar fecIngMinima = (Calendar) fecNacCalendar.clone();
            fecIngMinima.add(Calendar.YEAR, 18);
            if (calendar.before(fecIngMinima)) {
                errorMessageBuilder.append("La fecha de ingreso debe ser al menos 18 años posterior a la fecha de nacimiento\n");
                isValidForm = false;
            }
        } else {
            errorMessageBuilder.append("La fecha de ingreso no puede estar vacía\n");
            isValidForm = false;
        }
        
        
        if(!isValidForm)
            JOptionPane.showMessageDialog(this, errorMessageBuilder.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        
        return isValidForm;
    }
